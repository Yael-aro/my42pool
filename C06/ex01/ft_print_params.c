#include<unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;
	int	a;

	j = 1;
	i = argc -1;
	a = 0;
	while (j <= i)
	{
		a = 0;
		while (argv[j][a] != '\0')
		{
			write(1, &argv[j][a], 1);
			a++;
		}
		write(1, "\n", 1);
		j++;
	}
}
